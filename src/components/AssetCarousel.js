const AssetCarousel = ({ assets }) => {
  // {currentProject?.assets?.youtube && (
  const theme = useTheme();
  const [activeStep, setActiveStep] = useState(0);
  console.log("assets", assets);
  let maxSteps = 0;
  if (assets?.youtube) maxSteps++;
  if (assets?.images) maxSteps += assets.images.length;

  console.log("maxSteps", maxSteps);

  const handleNext = () => {
    setActiveStep((prevActiveStep) => prevActiveStep + 1);
  };

  const handleBack = () => {
    setActiveStep((prevActiveStep) => prevActiveStep - 1);
  };

  const handleStepChange = (step) => {
    setActiveStep(step);
  };
  const showAsset = (asset) => {
    if (activeStep === 0 && assets?.youtube) {
      return (
        <iframe
          width="560"
          height="315"
          src={assets?.youtube.split(".be/").join("be.com/embed/")}
          title="YouTube video player"
          frameborder="0"
          allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture"
          allowfullscreen
        ></iframe>
      );
    }
  };
  return (
    <Box sx={{ flexGrow: 1, padding: "50px" }}>
      {(() => showAsset())()}
      {assets?.images?.map((step, index) => {
        console.log("step", step);
        console.log("index", index);
        console.log("activeStep", activeStep);

        if (index === activeStep - 1) {
          return (
            <div
              key={index}
              style={{
                display: "flex",
                alignContent: "center",
                justifyContent: "center",
              }}
            >
              <Box
                component="img"
                sx={{
                  height: 255,
                  display: "block",
                  maxWidth: 400,
                  overflow: "hidden",
                  width: "100%",
                }}
                src={step}
              />
            </div>
          );
        }
      })}
      <MobileStepper
        steps={maxSteps}
        position="static"
        activeStep={activeStep}
        nextButton={
          <Button
            size="small"
            onClick={handleNext}
            disabled={activeStep === maxSteps - 1}
          >
            Next
            {theme.direction === "rtl" ? (
              <KeyboardArrowLeft />
            ) : (
              <KeyboardArrowRight />
            )}
          </Button>
        }
        backButton={
          <Button size="small" onClick={handleBack} disabled={activeStep === 0}>
            {theme.direction === "rtl" ? (
              <KeyboardArrowRight />
            ) : (
              <KeyboardArrowLeft />
            )}
            Back
          </Button>
        }
      />
    </Box>
  );
};

export default AssetCarousel;
